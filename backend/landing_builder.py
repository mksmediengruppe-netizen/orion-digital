def _get_photo_tag(section_id, photo_map):
    for key, url in photo_map.items():
        if key.startswith(section_id):
            return f'<img src="images/{key}" alt="{section_id}" loading="lazy">'
    return f'<img src="images/{section_id}.png" alt="{section_id}" loading="lazy">'


def _section_nav_name_reverse(name):
    mapping = {
        "О нас": "about", "Услуги": "services", "Портфолио": "portfolio",
        "Тарифы": "pricing", "Отзывы": "reviews", "Команда": "team",
        "FAQ": "faq", "Контакты": "contacts", "Преимущества": "advantages",
        "Как мы работаем": "process", "Галерея": "gallery", "Партнёры": "partners", "Блог": "blog",
    }
    return mapping.get(name, name.lower())
