"""
Landing Builder — Генерирует полный лендинг по blueprint + content + design_spec.
Создаёт HTML, CSS, JS, PHP handler, генерирует AI фото.
Выход: директория с файлами + build_report.json
"""
import json
import logging
import os
import re
from typing import Optional, Callable

logger = logging.getLogger(__name__)


def build_landing(
    blueprint: dict,
    content: dict,
    design_spec: dict,
    output_dir: str,
    generate_image_fn: Optional[Callable] = None,
    ssh_fn: Optional[Callable] = None,
) -> dict:
    """
    Генерирует полный лендинг.
    
    Args:
        blueprint: site_blueprint.json
        content: site_content.json (тексты для каждой секции)
        design_spec: site_design_spec.json
        output_dir: Директория для сохранения файлов
        generate_image_fn: Функция генерации AI-фото (prompt -> url)
        ssh_fn: Функция SSH для деплоя на сервер
    
    Returns:
        dict: build_report с результатами
    """
    os.makedirs(output_dir, exist_ok=True)
    os.makedirs(os.path.join(output_dir, "images"), exist_ok=True)
    
    report = {
        "files_created": [],
        "photos_generated": 0,
        "photos_failed": 0,
        "errors": [],
        "status": "building",
    }
    
    colors = design_spec.get("colors", {})
    typography = design_spec.get("typography", {})
    fonts = design_spec.get("google_fonts", ["Inter"])
    
    # ── 1. Генерируем CSS ────────────────────────────────────
    css = _generate_css(blueprint, design_spec)
    css_path = os.path.join(output_dir, "style.css")
    with open(css_path, "w", encoding="utf-8") as f:
        f.write(css)
    report["files_created"].append("style.css")
    
    # ── 2. Генерируем JS ─────────────────────────────────────
    js = _generate_js(blueprint)
    js_path = os.path.join(output_dir, "main.js")
    with open(js_path, "w", encoding="utf-8") as f:
        f.write(js)
    report["files_created"].append("main.js")
    
    # ── 3. Генерируем PHP handler ────────────────────────────
    php = _generate_php_handler(blueprint)
    php_path = os.path.join(output_dir, "send.php")
    with open(php_path, "w", encoding="utf-8") as f:
        f.write(php)
    report["files_created"].append("send.php")
    
    # ── 4. Генерируем AI фото ────────────────────────────────
    photo_map = {}
    if generate_image_fn and blueprint.get("photo_prompts"):
        for pp in blueprint["photo_prompts"]:
            try:
                variant = pp.get("variant", 0)
                filename = f"{pp['section']}_{variant}.png" if variant else f"{pp['section']}.png"
                url = generate_image_fn(pp["prompt"])
                photo_map[filename] = url
                report["photos_generated"] += 1
                logger.info(f"[LandingBuilder] Photo generated: {filename}")
            except Exception as e:
                report["photos_failed"] += 1
                report["errors"].append(f"Photo {pp['section']}: {e}")
                logger.warning(f"[LandingBuilder] Photo failed: {pp['section']}: {e}")
    
    # ── 5. Генерируем HTML ───────────────────────────────────
    html = _generate_html(blueprint, content, design_spec, photo_map)
    html_path = os.path.join(output_dir, "index.html")
    with open(html_path, "w", encoding="utf-8") as f:
        f.write(html)
    report["files_created"].append("index.html")
    
    # ── 6. Privacy page ──────────────────────────────────────
    privacy = _generate_privacy_page(blueprint, design_spec)
    privacy_path = os.path.join(output_dir, "privacy.html")
    with open(privacy_path, "w", encoding="utf-8") as f:
        f.write(privacy)
    report["files_created"].append("privacy.html")
    
    # ── 7. Build report ──────────────────────────────────────
    report["status"] = "complete"
    report["photo_map"] = photo_map
    report_path = os.path.join(output_dir, "build_report.json")
    with open(report_path, "w", encoding="utf-8") as f:
        json.dump(report, f, ensure_ascii=False, indent=2)
    report["files_created"].append("build_report.json")
    
    logger.info(f"[LandingBuilder] Build complete: {len(report['files_created'])} files, "
                f"{report['photos_generated']} photos, {len(report['errors'])} errors")
    return report


